package org.java.loop;

public class While3 {
	public static void main(String[] args) {
		System.out.println("do~while¹®");

		for (int i2 = 2; i2 < 1; i2++) {
			System.out.println(i2);
		}
		int i2 = 2;
		while (i2 < 1) {
			System.out.println(i2);
		}
		int i3 = 2;
		do {
			System.out.println(i3);
		} while (i3 < 1);
	}

}
